export function getClientPageContent(pageKey, locale = DEFAULT_LOCALE) {
  const resolvedLocale = resolveLocale(locale);
  const pageContent = clientPageContent[pageKey];

  if (!pageContent || resolvedLocale === DEFAULT_LOCALE) {
    return pageContent ?? null;
  }

  const localizedBase = localizeContent(pageContent, resolvedLocale);

  return mergeLocalizedContent(localizedBase, clientPageTranslations[resolvedLocale]?.[pageKey]);
}
